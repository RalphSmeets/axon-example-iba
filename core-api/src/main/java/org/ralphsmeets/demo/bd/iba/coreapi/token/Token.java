package org.ralphsmeets.demo.bd.iba.coreapi.token;

public class Token {
    private String tokenId;
    private String token;

    public Token() {}

    public Token(String tokenId,  String token) {
        this.tokenId = tokenId;
        this.token = token;
    }

    public String getTokenId() {
        return token;
    }

    public String getToken() {
        return token;
    }
}
